<h1>Press</h1>

<p>Since opening in 2005, Salaam Namaste has won many accolades and has established itself as one of the best Indian restaurants in London, regularly noted as ‘a hidden gem’ and ‘a Bloomsbury favourite’ by luminaries and has received glowing reviews from guests and Critics alike – <b>Trip Advisor, BBC, The Guardian, Hardens and the Evening Standard</b>. <br /><br />
The most recent being Chef Sabbir Karim Winner Of <b>The Best Innovative Chef Of The Year 2013</b> and Salaam Namaaste Bloombury won <b>The Best Newcomer</b> in the prestigious Asian Curry Awards 2013 held at the London's Mayfair, Grosvenor House Hotel.
<br /><br />
<b>TripAdvisor Certificate of Excellence 2013</b><br /> 

We are very proud to announce that we have been awarded a Certificate of Excellence 2013 by TripAdvisor, which is given to businesses that consistently earn high ratings from Trip Advisor Reviewers and places us in the top-performing 10% of all businesses worldwide on Trip Advisor. A big thank you to all of our lovely customers for your kind reviews on TripAdvisor.
</p>

